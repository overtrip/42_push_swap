int	ft_atoi(char *str)
{
	int	s;
	int	nb;

	nb = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		++str;
	s = (*str == '-' ? -1 : 1);
	(*str == '+' || *str == '-') ? ++str : str;
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + *str - 48;
		++str;
	}
	return (s * nb);
}
